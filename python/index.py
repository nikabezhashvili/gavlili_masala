# სიის მეთოდები
# append() - გამოიყენება სიაში ახალი ელემენტის დამატებისთვის

# მაგ
my_list = ['string', 2, 3.5, True, [1, 2, 3]]
my_list.append("new element")

# insert() - გამოიყენება სიაში ახალი ელემენტის დამატებისთვის კონკრეტულ ადგილას

# მაგ
my_list = ['string', 2, 3.5, True, [1, 2, 3]]
my_list.insert(2, "new element") # ეს დაამატებს "new element"-ს 2-ე ინდექსზე ანუ 3.5-ის ადგილას და 3.5 გადაინაცვლებს 3-ე ინდექსზე

# remove() - გამოიყენება სიიდან ელემენტის წასაშლელად

# მაგ
my_list = ['string', 2, 3.5, True, [1, 2, 3]]
my_list.remove(2) # ეს წაშლის 2-ს სიიდან


# pop() - გამოიყენება სიიდან ელემენტის წასაშლელად და მისი მნიშვნელობის დაბრუნებისთვის

# მაგ
my_list = ['string', 2, 3.5, True, [1, 2, 3]]
removed_element = my_list.pop(1) # ეს წაშლის 1-ე ინდექსზე მყოფ ელემენტს ანუ 2-ს და დააბრუნებს მას
print(removed_element) # შედეგი იქნება 2

# clear() - გამოიყენება სიიდან ყველა ელემენტის წასაშლელად

# მაგ
my_list = ['string', 2, 3.5, True, [1, 2, 3]]
my_list.clear() # ეს წაშლის ყველა ელემენტს სიიდან და სია გახდება ცარიელი

# index() - გამოიყენება სიაში მყოფი ელემენტის ინდექსის დასადგენად

# მაგ
my_list = ['string', 2, 3.5, True, [1, 2, 3]]
print(my_list.index(3.5)) # შედეგი იქნება 2 (რადგან 3.5 არის 2-ე ინდექსზე)

# count() - გამოიყენება სიაში გარკვეული ელემენტის რაოდენობის დასათვლელად

# მაგ
my_list = ['string', 2, 3.5, True, [1, 2, 3], 2]
print(my_list.count(2)) # შედეგი იქნება 2 (რადგან 2 არის სიაში 2-ჯერ)

# sort() - გამოიყენება სიაში ელემენტების ზრდის ან კლების მიხედვით დალაგებისთვის

# მაგ
my_list = [3, 1, 4, 2]  
my_list.sort() # ეს დალაგებს ელემენტებს ზრდის მიხედვით
print(my_list) # შედეგი იქნება [1, 2, 3, 4]

# reverse() - გამოიყენება სიაში ელემენტების რიგის შეცვლისთვის (ზრდის -> კლების ან კლების -> ზრდის)

# მაგ
my_list = [1, 2, 3, 4]
my_list.reverse() # ეს შეცვლის ელემენტების რიგს კლების მიხედვით
print(my_list) # შედეგი იქნება [4, 3, 2, 1]

